# we can't modify the Tuples like the lists
# TUPLES are IMMUTABLE
numbers = (1, 2, 3)
# numbers[0] = 0 # TypeError: 'tuple' object does not support item assignment
print(numbers)

# Similar to list, we can access individual items in the tuples like a list using square brackets
# TypeError: 'tuple' object does not support item assignment
numbers[0]
