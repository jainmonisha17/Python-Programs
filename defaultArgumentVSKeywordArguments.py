class Demo:
    def disp(self, x = 111, y = 222, z = 333):
        print(x)
        print(y)
        print(z)

d = Demo()
a = 100
b = 200
c = 300

d.disp(a, b, c)
print("--------------------------------------------------------------------")
d.disp(a, b)
print("--------------------------------------------------------------------")
d.disp(a)
print("--------------------------------------------------------------------")
d.disp()
print("--------------------------------------------------------------------")
d.disp(b)
print("--------------------------------------------------------------------")
d.disp(b, c)
print("--------------------------------------------------------------------")
d.disp(y = b, z = c)
print("--------------------------------------------------------------------")